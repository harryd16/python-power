import turtle

window = turtle.Screen() # create a new turtle window
ella = turtle.Turtle() # create a new turtle called Ella
ella.speed(1) # set speed to 1 so we can see the animation

ella.forward(100) # move ella forward 100
ella.setheading(90) # set her heading to 90
ella.forward(100)
ella.setheading(180)
ella.forward(100)
ella.setheading(270)
ella.forward(100)
